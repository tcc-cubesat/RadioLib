int main(int argc, char** argv) {

  int state = radio.beginFSK();
  if (state == RADIOLIB_ERR_NONE){
    printf("success!\n");
  } else {
    printf("failed, code %i\n", state);
  }

  printf("Inicializando SSTV!\n");
  state = sstv.begin(915.0 ,Martin2);
  if (state == RADIOLIB_ERR_NONE){
    printf("success sstv!\n");
  } else {
    printf("failed, code %i\n", state);
  }

  printf("Setting correction ...");
  state = sstv.setCorrection(1.0);
  if (state == RADIOLIB_ERR_NONE){
    printf("success correction!\n");
  } else {
    printf("failed, code %i\n", state);
  }
    signal(SIGINT, sigHandler);
  while(keepRunning){

    printf("Sending test picture ...");
    hal->delay(1000);
    sstv.sendHeader();
    for (uint32_t i = 0; i < sstv.getPictureHeight(); i++){
      if (keepRunning){
      sstv.sendLine(line);
      } else {
        radio.reset();
        break;
      }
    }

    radio.standby();

    printf("Done!");
    hal->delay(1000);
    
  }
  return(0);
}
